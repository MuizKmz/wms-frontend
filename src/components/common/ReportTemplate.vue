      <slot></slot>
    </div>

    <!-- Summary Slot - Only show on last page -->
    <div v-if="$slots.summary && isLastPage" class="mt-4 mb-4 flex-shrink-0">
      <div class="bg-white p-4 max-w-xs">
        <h3 class="text-xs font-semibold text-gray-900 uppercase tracking-wide mb-3">Summary</h3>
        <slot name="summary"></slot>
      </div>
    </div>

    <!-- Report Footer -->
    <div 
      v-if="showFooter" 
      class="report-footer mt-auto border-t border-gray-300 flex-shrink-0"
      :class="orientation === 'landscape' ? 'pt-6 pb-2 px-4' : 'pt-4 px-4'"
