<script setup lang="ts">
import { ref, onMounted, computed } from 'vue'
import axios from '@/utils/axios'

interface SummaryCard {
  label: string
  value: string | number
  subtitle?: string
}

interface AppliedFilter {
  label: string
  value: string
}

const props = defineProps({
  title: {
    type: String,
    required: true
  },
  subtitle: {
    type: String,
    default: ''
  },
  orientation: {
    type: String as () => 'portrait' | 'landscape',
    default: 'portrait'
  },
  generatedDate: {
    type: Date,
    default: () => new Date()
  },
  appliedFilters: {
    type: Array as () => AppliedFilter[],
    default: () => []
  },
  summaryCards: {
    type: Array as () => SummaryCard[],
    default: () => []
  },
  companyName: {
    type: String,
    default: 'Warehouse Management System'
  },
  companyAddress: {
    type: String,
    default: 'Generated from WMS Platform'
  },
  generatedBy: {
    type: String,
    default: 'System Administrator'
  },
  logoUrl: {
    type: String,
    default: ''
  },
  logoSize: {
    type: Number,
    default: 80
  },
  showHeader: {
    type: Boolean,
    default: true
  },
  showTable: {
    type: Boolean,
    default: true
  },
  showFooter: {
    type: Boolean,
    default: true
  },
  referenceNo: {
    type: String,
    default: ''
  },
  hasData: {
    type: Boolean,
    default: true
  },
  pageIndex: {
    type: Number,
    default: 0
  },
  totalPages: {
    type: Number,
    default: 1
  }
})

defineEmits(['upload-logo'])

const currentUsername = ref('')

const isLastPage = computed(() => {
  return props.pageIndex === props.totalPages - 1
})

const formatDate = (date: Date) => {
  return date.toLocaleDateString('en-US', { 
    year: 'numeric', 
    month: 'long', 
    day: 'numeric' 
  })
}

const formatTime = (date: Date) => {
  return date.toLocaleTimeString('en-US', { 
    hour: '2-digit', 
    minute: '2-digit',
    hour12: true
  })
}

const fetchCurrentUser = async () => {
  try {
    const userStr = localStorage.getItem('user')
    if (userStr) {
      const user = JSON.parse(userStr)
      if (user.username) {
        currentUsername.value = user.username
      } else if (user.name) {
        currentUsername.value = user.name
      }
    }
  } catch (error) {
    console.error('Error fetching current user:', error)
  }
}

onMounted(() => {
  fetchCurrentUser()
})
</script>

